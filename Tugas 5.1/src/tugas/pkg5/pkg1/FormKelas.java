    private void tblTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tblTambahActionPerformed
        try {
            PreparedStatement ps = con.prepareStatement("INSERT INTO kelas VALUES (?,?)");
            ps.setString(1, inputKode.getText());
            ps.setString(2, inputKelas.getText());
            ps.executeUpdate();
            
            refresh();
            inputKode.setText("");
            inputKelas.setText("");
        } catch(Exception e) {
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_tblTambahActionPerformed

